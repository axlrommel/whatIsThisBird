package com.villagomezdiaz.common.ds;

public class BirdAndSource {
	private String source;
	private String bird;
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getBird() {
		return bird;
	}
	public void setBird(String bird) {
		this.bird = bird;
	}
	public BirdAndSource(String source, String bird) {
		super();
		this.source = source;
		this.bird = bird;
	}
	
	

}
